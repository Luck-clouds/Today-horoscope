// 清理过期IP记录的定时任务
const ipStore = new Map();
setInterval(() => {
  const now = Date.now();
  for (const [ip, data] of ipStore.entries()) {
    // 清理过期的请求记录
    data.requests = data.requests.filter((time) => now - time < WINDOW_MS);

    // 清理过期的封禁记录
    if (data.blockedUntil && now > data.blockedUntil) {
      ipStore.delete(ip);
    }

    // 如果没有任何活动记录，删除IP
    if (data.requests.length === 0 && !data.blockedUntil) {
      ipStore.delete(ip);
    }
  }
}, 60 * 1000); // 每分钟清理一次

// IP限流中间件
function rateLimit(req, res, next) {
  const clientIP = getClientIP(req);
  const now = Date.now();

  // 初始化IP记录
  if (!ipStore.has(clientIP)) {
    ipStore.set(clientIP, {
      requests: [],
      blockedUntil: null,
    });
  }

  const ipData = ipStore.get(clientIP);

  // 检查是否被封禁
  if (ipData.blockedUntil && now < ipData.blockedUntil) {
    const remainingTime = Math.ceil((ipData.blockedUntil - now) / 1000 / 60);
    return res.status(429).json({
      code: 429,
      status: "访问过于频繁喵",
      error: `您的IP已被暂时限制访问，请${remainingTime}分钟后再试`,
      retryAfter: remainingTime,
    });
  }

  // 清理过期的请求记录
  ipData.requests = ipData.requests.filter((time) => now - time < WINDOW_MS);

  // 检查请求频率
  if (ipData.requests.length >= MAX_REQUESTS_PER_MINUTE) {
    // 封禁IP
    ipData.blockedUntil = now + BLOCK_DURATION;
    ipStore.set(clientIP, ipData);

    console.log(`IP ${clientIP} 因频繁请求被封禁5分钟`);

    return res.status(429).json({
      code: 429,
      status: "访问过于频繁",
      error: "您的请求过于频繁，IP已被暂时限制访问，请15分钟后再试",
      retryAfter: 15,
    });
  }

  // 记录本次请求
  ipData.requests.push(now);
  ipStore.set(clientIP, ipData);

  next();
}

// 获取客户端真实IP的中间件
function getClientIP(req) {
  return (
    req.headers["x-forwarded-for"] ||
    req.connection.remoteAddress ||
    req.socket.remoteAddress ||
    (req.connection.socket ? req.connection.socket.remoteAddress : null)
  );
}

