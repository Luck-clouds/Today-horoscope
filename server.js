// 获取详细农历信息 仅支持到2100年
function lunarData(date) {
  try {
    const year = date.year;
    const month = date.month;
    const day = date.day;

    const lunarDataInfo = LunarCalendar.solarToLunar(year, month, day);

    // 构建农历数据
    const lunarData = {
      // 干支记年
      ganZhiYear: lunarDataInfo.GanZhiYear,
      // 干支纪月
      ganZhiMonth: lunarDataInfo.GanZhiMonth,
      // 干支纪日
      ganZhiDay: lunarDataInfo.GanZhiDay,
      //  农历月
      lunarMonth: lunarDataInfo.lunarMonthName,
      // 农历日
      lunarDay: lunarDataInfo.lunarDayName,
      // 生肖
      zodiac: lunarDataInfo.zodiac,
      // 二十四节气
      solarTerm: lunarDataInfo.term,
    };

    return lunarData;
  } catch (error) {
    console.log("获取农历失败", error);

    return {
      ganZhiYear: "未知",
      ganZhiMonth: "未知",
      ganZhiDay: "未知",
      lunarMonth: "未知",
      lunarDay: "未知",
      zodiac: "未知",
      solarTerm: "未知",
    };
  }
}

function formatLunar(lunarData) {
  return `${lunarData.ganZhiYear}年${lunarData.zodiac} ${lunarData.ganZhiMonth} ${lunarData.ganZhiDay} 农历${lunarData.lunarMonth}${lunarData.lunarDay} ${lunarData.solarTerm}`;
}

